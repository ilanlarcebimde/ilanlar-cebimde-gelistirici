export type CountryKey = "katar" | "irlanda" | "belcika" | "usa" | null;
export function getCountryFromLocation(locationText: string | null | undefined): CountryKey {
  if (!locationText || typeof locationText !== "string") return null;
  const t = locationText.trim().toLowerCase();
  if (/katar|qatar/.test(t)) return "katar";
  if (/irlanda|ireland/.test(t)) return "irlanda";
  if (/belçika|belgium/.test(t)) return "belcika";
  if (/alaska|amerika|abd|usa|a\.b\.d|america|united states|eyaleti|texas|california|florida|nevada|new york/i.test(t)) return "usa";
  return null;
}

